// Utility functions for data processing
export const getDepartments = () => {
  return [...new Set(employees.map(emp => emp.department))].sort();
};

export const getTotalEmployees = () => {
  return employees.length;
};

export const getTotalDepartments = () => {
  return getDepartments().length;
};

export const getNewEmployeesThisMonth = () => {
  const currentDate = new Date();
  const currentMonth = currentDate.getMonth();
  const currentYear = currentDate.getFullYear();
  
  return employees.filter(emp => {
    const joinDate = new Date(emp.joinDate);
    return joinDate.getMonth() === currentMonth && joinDate.getFullYear() === currentYear;
  }).length;
};

export const getAverageSalary = () => {
  const total = employees.reduce((sum, emp) => {
    const salary = parseInt(emp.salary.replace(/[^0-9]/g, ''));
    return sum + salary;
  }, 0);
  return Math.round(total / employees.length);
};